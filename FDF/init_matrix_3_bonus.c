#include "fdf_bonus.h"

void	init_r_cabinet(float m[4][4])
{
	float	d;

	d = PI / 6.0f;
	m[0][0] = 1.0f;
	m[0][1] = -0.5f;
	m[0][2] = 0.0f;
	m[0][3] = 0.0f;
	m[1][0] = 0.0f;
	m[1][1] = cos(d);
	m[1][2] = -sin(d);
	m[1][3] = 0.0f;
	m[2][0] = 0.0f;
	m[2][1] = sin(d);
	m[2][2] = cos(d);
	m[2][3] = 0.0f;
	m[3][0] = 0.0f;
	m[3][1] = 0.0f;
	m[3][2] = 0.0f;
	m[3][3] = 1.0f;
}
